    private $opts = null;
    private $captcha = null;
    private $hmac = null;

    public function __construct()
    {
        $this->captcha = null;
        $this->hmac = null;
        $this->opts = array();
        $this
            ->option('secret_key', 'SECRET_KEY')
            ->option('secret_salt', 'SECRET_SALT_')
            ->option('difficulty', 1) // 1 (easy) to 3 (difficult)
            ->option('num_terms', 2)
            ->option('min_term', 1)
            ->option('max_term', 20)
            ->option('color', 0x121212) // text color
            ->option('background', 0xffffff) // background color
        ;
    }

    public function option($key, $val = null)
    {
        $nargs = func_num_args();
        if (1 == $nargs)
        {
            return isset($this->opts[$key]) ? $this->opts[$key] : null;
        }
        elseif (1 < $nargs)
        {
            $this->opts[$key] = $val;
        }
        return $this;
    }

    public function getCaptcha()
    {
        if (empty($this->captcha)) $this->generate();
        return $this->captcha;
    }

    public function getHash()
    {
        if (empty($this->captcha)) $this->generate();
        return $this->hmac;
    }

    public function reset()
    {
        $this->captcha = null;
        $this->hmac = null;
        return $this;
    }

    public function validate($answer = null, $hmac = null)
    {
        if (is_null($answer) || empty($hmac)) return false;
        $algo = function_exists('hash') ? 'sha256' : 'sha1';
        $hash = hash_hmac($algo, (string)$this->option('secret_salt') . (string)$answer, (string)$this->option('secret_key'));
        return hash_equals($hash, (string)$hmac);
    }

    private function generate()
    {
        $difficulty = min(3, max(1, (int)$this->option('difficulty')));
        $num_terms = max(2, (int)$this->option('num_terms'));
        $min_term = max(0, (int)$this->option('min_term'));
        $max_term = max(0, (int)$this->option('max_term'));
        $color = (int)$this->option('color');
        $background = (int)$this->option('background');

        // generate mathematical formula
        list($formula, $result) = $this->formula($num_terms, $min_term, $max_term, $difficulty);

        // compute hmac of result
        $algo = function_exists('hash') ? 'sha256' : 'sha1';
        $this->hmac = hash_hmac($algo, (string)($this->option('secret_salt') ? $this->option('secret_salt') : '') . (string)$result, $this->option('secret_key'));

        // create image captcha with formula depending on difficulty
        $captcha = $this->image($formula, $color, $background, $difficulty);

        // output image
        ob_start(); imagepng($captcha);
        $this->captcha = 'data:image/png;base64,' . base64_encode(ob_get_clean());

        // free memory
        imagedestroy($captcha);

        return $this;
    }

    private function formula($terms, $min, $max, $difficulty)
    {
        // generate mathematical formula
        $formula = array();
        $result = 0;
        $factor = 0;
        for ($i=0; $i<$terms; ++$i)
        {
            $x = (int)mt_rand($min, $max);
            if (($result > $x) && mt_rand(0, 1))
            {
                // randomly use plus or minus operator
                $x = -$x;
            }
            elseif ((1 < $difficulty) && ($x <= 5) && mt_rand(0, 1))
            {
                // randomly use multiplication factor
                $factor = (int)mt_rand(2, 3);
            }
            if (0 < $factor)
            {
                $result += $x * $factor;
                if (0 > $x)
                {
                    $formula[] = '-';
                    $formula = array_merge($formula, str_split((string)abs($x)));
                    $formula[] = '×';
                    $formula = array_merge($formula, str_split((string)$factor));
                }
                else
                {
                    if (0 < $i) $formula[] = '+';
                    $formula = array_merge($formula, str_split((string)$x));
                    $formula[] = '×';
                    $formula = array_merge($formula, str_split((string)$factor));
                }
            }
            else
            {
                $result += $x;
                if (0 > $x)
                {
                    $formula[] = '-';
                    $formula = array_merge($formula, str_split((string)abs($x)));
                }
                else
                {
                    if (0 < $i) $formula[] = '+';
                    $formula = array_merge($formula, str_split((string)$x));
                }
            }
            $factor = 0;
        }
        return array($formula, $result);
    }

    private function image($chars, $color, $background, $difficulty)
    {
        $bitmaps = $this->chars();

        $cw = $bitmaps['width'];
        $ch = $bitmaps['height'];
        $n = count($chars);
        $space = 1;
        $x0 = 10;
        $y0 = 10;
        $w = $n * $cw + ($n-1) * $space + 2 * $x0;
        $h = $ch + 2 * $y0;

        $r0 = ($background >> 16) & 0xff;
        $g0 = ($background >> 8) & 0xff;
        $b0 = ($background) & 0xff;
        $r = ($color >> 16) & 0xff;
        $g = ($color >> 8) & 0xff;
        $b = ($color) & 0xff;

        // img bitmap
        $imgbmp = array_fill(0, $w*$h, (($r0 << 16) | ($g0 << 8) | ($b0)) & 0xffffff);

        // render chars
        foreach ($chars as $char)
        {
            $charbmp =& $bitmaps['chars'][$char]['bitmap'];
            for ($x=0; $x<$cw; ++$x)
            {
                for ($y=0; $y<$ch; ++$y)
                {
                    $alpha = $charbmp[$x + $cw*$y];
                    if (0 < $alpha)
                    {
                        $alpha = (float)$alpha / 255.0;
                        $imgbmp[$x0+$x + $w*($y0+$y)] = ((intval($r0*(1-$alpha) + $alpha*$r) & 0xff) << 16) | (((intval($g0*(1-$alpha) + $alpha*$g) & 0xff) << 8)) | ((intval($b0*(1-$alpha) + $alpha*$b) & 0xff)) & 0xffffff;
                    }
                }
            }
            $x0 += $cw + $space;
        }

        // create distorted GD image based on difficulty level
        $img = imagecreatetruecolor($w, $h);
        $phase = (float)mt_rand(0, 2) * 3.14 / 2.0;
        $amplitude = 3 == $difficulty ? 5.0 : (2 == $difficulty ? 3.0 : 1.5);
        for ($y=0; $y<$h; ++$y)
        {
            $y0 = $y;
            for ($x=0; $x<$w; ++$x)
            {
                $x0 = $x;
                $y0 = max(0, min($h-1, round($y + $amplitude * sin($phase + 6.28 * 2 * $x / $w))));
                $c = $imgbmp[$x0 + $w*$y0];
                imagesetpixel($img, $x, $y, imagecolorallocate($img, ($c >> 16) & 0xff, ($c >> 8) & 0xff, ($c) & 0xff));
            }
        }

        // free memory
        $bitmaps = null;
        $imgbmp = null;

        return $img;
    }

